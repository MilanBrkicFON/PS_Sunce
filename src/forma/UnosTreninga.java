    private void srediFormu() {
        jtxtDatum.setText(datum.format(DateTimeFormatter.ofPattern("dd.MM.yyyy - EEEE", new Locale("sr", "RS"))));
        jtxtVremePocetka.setText(vremeOd.format(DateTimeFormatter.ISO_TIME));
        jtxtVremeKraja.setText(vremeDo.format(DateTimeFormatter.ISO_TIME));
    }

    private void popuniCombo() throws Exception {
        try {
            RequestObject req = new RequestObject();
            req.setAction(Akcije.VRATI_SVA_SPORTOVE);

            Komunikacija.vratiInstancu().posaljiZahtev(req);

            ResponseObject res = Komunikacija.vratiInstancu().procitajOdgovor();

            if (res.getObject() instanceof List) {
                List<Sport> sportovi = (List<Sport>) res.getObject();
                sportovi.forEach(s -> jComboBox1.addItem(s));

            }
            RequestObject req1 = new RequestObject();
            req1.setAction(Akcije.VRATI_SVE_TRENERE);

            Komunikacija.vratiInstancu().posaljiZahtev(req1);

            res = Komunikacija.vratiInstancu().procitajOdgovor();

            if (res.getObject() instanceof List) {
                treneri = (List<Trener>) res.getObject();
            }
            jListOdabraniTreneri.setModel(new DefaultListModel<>());
            jListSviTreneri.setModel(new DefaultListModel<>());
        } catch (IOException | ClassNotFoundException ex) {
            ex.printStackTrace();
            throw new Exception("Greska prilikom popunjavanja forme od strane servera.");
        }
    }

