    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            String dat = datum.format(DateTimeFormatter.ISO_DATE);
            datum = LocalDate.parse(dat,DateTimeFormatter.ISO_DATE);

            vremeOd = LocalTime.parse(jtxtVremePocetka.getText().trim(), DateTimeFormatter.ISO_TIME);
            vremeDo = LocalTime.parse(jtxtVremeKraja.getText().trim(), DateTimeFormatter.ISO_TIME);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Pogresan format datuma");
            return;
        }
        Trening t= new Trening(vremeOd, vremeDo, datum);
        List<Trener> odabraniTreneri = new ArrayList<>();
        DefaultListModel m = (DefaultListModel) jListOdabraniTreneri.getModel();
        
        for(int i = 0; i< m.getSize();i++){
            odabraniTreneri.add((Trener) m.getElementAt(i));
        }
        
        if(!odabraniTreneri.isEmpty()){
            t.setTreneri(odabraniTreneri);
        }
        try {
            Komunikacija.vratiInstancu().posaljiZahtev(new RequestObject(t, Akcije.KREIRAJ_TRENING));
            
            ResponseObject res = Komunikacija.vratiInstancu().procitajOdgovor();
            
            if (res.getStatus() == EnumResponseStatus.ERROR) {
                JOptionPane.showMessageDialog(this, res.getMessage());
                return;
            }
            JOptionPane.showMessageDialog(this, "Uspesno ste sacuvali trening");
        } catch (IOException ex) {
            Logger.getLogger(UnosTreninga.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(UnosTreninga.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

