    private void jBtnObrisiClanaSaTreningaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnObrisiClanaSaTreningaActionPerformed

        int odg = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da želite da obrišete selektovane članove sa treninga?", JOptionPane.OPTION_TYPE_PROPERTY, JOptionPane.YES_NO_OPTION);
        if (odg == JOptionPane.YES_OPTION) {
            try {
                List<Object> pom = new ArrayList<>();
                List<Clan> clanovi = jListClanova.getSelectedValuesList();
                pom.add(clanovi);
                pom.add(Memory.getInstance().getTrening());
                RequestObject req = new RequestObject();
                req.setObject(pom);
                req.setAction(Akcije.OBRISI_CLANOVE_SA_TRENINGA);

                Komunikacija.vratiInstancu().posaljiZahtev(req);

                ResponseObject res = Komunikacija.vratiInstancu().procitajOdgovor();

                if (res.getStatus() == EnumResponseStatus.ERROR) {
                    JOptionPane.showMessageDialog(this, res.getMessage(), "Greška", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                ((ListModelClanovi) jListClanova.getModel()).obrisi(clanovi);
                refresh();
                JOptionPane.showMessageDialog(this, res.getMessage(), "Uspešna akcija", JOptionPane.INFORMATION_MESSAGE);
            } catch (IOException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Greška", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jBtnObrisiClanaSaTreningaActionPerformed

