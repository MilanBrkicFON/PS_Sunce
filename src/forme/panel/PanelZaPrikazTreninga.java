    public void refresh() {
        if (!Memory.getInstance().getTrening().getClanovi().isEmpty() && !Memory.getInstance().getTrening().getTreneri().isEmpty()) {
            int popunjenost = new BigDecimal((Memory.getInstance().getTrening().getClanovi().size() * 100.0) / Memory.getInstance().getTrening().getTreneri().get(0).getSport().getMaxBrClanova()).setScale(0, RoundingMode.HALF_UP).intValue();
            jProgressBarPopunjenostTreninga.setValue(popunjenost);
        } else {
            jProgressBarPopunjenostTreninga.setValue(0);
        }
    }

